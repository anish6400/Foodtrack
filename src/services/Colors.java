package services;

public class Colors {
	
	private String primaryBackgroundColor = "#E5EBEB";
	private String secondaryBackgroundColor = "#FFFFFF";
	private String primaryTextColor = "#333333";
	private String secondaryTextColor = "#808080";
	private String warningColor = "#ED4949";
	private String accentColor = "#577CFF";
	
	public Colors() {}

	public String getPrimaryBackgroundColor() {
		return primaryBackgroundColor;
	}

	public String getSecondaryBackgroundColor() {
		return secondaryBackgroundColor;
	}

	public String getPrimaryTextColor() {
		return primaryTextColor;
	}

	public String getSecondaryTextColor() {
		return secondaryTextColor;
	}

	public String getWarningColor() {
		return warningColor;
	}

	public String getAccentColor() {
		return accentColor;
	}
	
	
}
